def find_exit(maze):
    #Scan all sides of the maze for an empty space
    for i in range(27):
        if maze[0][i] == " ":
            return [0,i] #Return the coordinates to the variable calling the function
    for i in range(11):
        if maze[i][0] == " ":
            return [i,0]
    for i in range(11):
        if maze[i][26] == " ":
            return [i,26]
    for i in range(27):
        if maze[10][i] == " ":
            return [10,i]

#Find player
def find_player(maze):
    for i in range(11):
        for j in range(27):
            if maze[i][j] == "p":
                return [i,j]

#Print the maze
def print_maze(maze):
    for i in range(11):
        lane_to_print = ""
        for j in range(27):
            lane_to_print += maze[i][j]
        print(lane_to_print)

def display_maze(maze):
    total = []
    for i in range(27):
        lane_to_print = ""
        for j in range(11):
            lane_to_print += maze[j][i]
            lane_to_print += "\n"
        total.append(lane_to_print)
    return total
    
